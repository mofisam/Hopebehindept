<!-- Counter Animation Script -->
<script>
  document.addEventListener('DOMContentLoaded', function() {
    const counters = document.querySelectorAll('.counter');
    const speed = 200;
    
    counters.forEach(counter => {
      const target = +counter.getAttribute('data-target');
      const count = +counter.innerText;
      const increment = target / speed;
      
      if (count < target) {
        const updateCount = () => {
          const current = +counter.innerText;
          const newCount = Math.ceil(current + increment);
          
          if (current < target) {
            counter.innerText = newCount;
            setTimeout(updateCount, 1);
          } else {
            counter.innerText = target;
            // Format numbers
            if (counter.classList.contains('text-info')) {
              counter.innerText = '₦' + target + 'M';
            } else if (counter.classList.contains('text-warning')) {
              counter.innerText = target + '%';
            } else if (counter.classList.contains('text-primary') && target > 1000) {
              counter.innerText = target.toLocaleString();
            }
          }
        };
        
        const observer = new IntersectionObserver((entries) => {
          if (entries[0].isIntersecting) {
            updateCount();
            observer.unobserve(counter);
          }
        });
        
        observer.observe(counter);
      }
    });
  });
</script>